import axios from "axios";

export function getRandomCode() {
  const keys = Object.keys(testData);
  const randomIndex = Math.floor(Math.random() * keys.length);
  const code = keys[randomIndex];
  return code;
}

export async function lookupCode() {
  const response = await axios.post(
    "http://localhost:8080/terminology/code-system/lookup",
    {
      resourceType: "Parameters",
      parameter: [
        { name: "system", valueUri: "http://snomed.info/sct" },
        { name: "code", valueCode: getRandomCode() },
      ],
    },
    {
      headers: {
        "Content-Type": "application/json",
      },
    }
  );
  return response.data;
}

export async function translateCode() {
  const system = "http://snomed.info/sct";
  const code = getRandomCode();
  const response = await axios.post(
    "http://localhost:8080/terminology/concept-map/translate",
    {
      resourceType: "Parameters",
      parameter: [
        { name: "system", valueUri: system },
        { name: "code", valueCode: code },
        { name: "targetsystem", valueUri: "http://hl7.org/fhir/sid/icd-10-cm" },
        { name: "coding", valueCoding: { system, code } },
      ],
    },
    {
      headers: {
        "Content-Type": "application/json",
      },
    }
  );
  return response.data;
}

